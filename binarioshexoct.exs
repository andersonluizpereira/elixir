#Binários, hexadecimais e octais
#Elixir possui diferentes prefixos para converter determinadas bases numéricas em outras. 
#Para converter de binário para decimal, nós podemos utilizar o prefixo 0b .
iex(1)> 0b1111
15
#Para converter números hexadecimais em decimais, temos o prefixo 0x .
iex(2)> 0xF
15
#De números octais para decimais, temos o 0o . 
iex(3)> 0o17
15